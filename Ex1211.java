/*Execise 1.2.11 What gets printed with these output commands if a equals 3.14159? */
public class Ex1211
{
	public static void main(String[] args)
	{
		double a = 3.14159;
		System.out.println("a equals " + a);
		System.out.println("a + 1 equals " + (a + 1));
		System.out.println("8/(int) a equals " + (8/(int)a));
		System.out.println("8/a equals " + (8/a));
		System.out.println("(int)(8/a) equals " + (int)(8/a));
	}
}
