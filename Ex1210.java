/*Execise 1.2.8 Find the square root of a number */
public class Ex1210
{
	public static void main(String[] args)
	{
		int a = 2147483647;
		System.out.println("a equals " + a);
		System.out.println("a + 1 equals " + (a + 1));
		System.out.println("2 - a equals " + (2 - a));
		System.out.println("-2 - a equals " + (-2 - a));
		System.out.println("2 * a equals " + (2*a));
		System.out.println("4 * a equals " + (4*a));
	}
}
